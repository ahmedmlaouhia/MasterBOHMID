export function getQuote(l: string) {
  switch (l) {
    case 'A':
      return 'Always learning, always coding.'
    case 'S':
      return 'Striving for excellence in everything I do.'
    case 'T':
      return 'Tech enthusiast by day, coding ninja by night.'
    case 'E':
      return 'Engineering solutions to complex problems.'
    case 'R':
      return 'Relentlessly pursuing innovation and efficiency.'
    case '_':
      return 'Blank canvas, ready to create.'
    case 'B':
      return 'Building scalable and resilient systems.'
    case 'O':
      return 'Optimizing processes for maximum performance.'
    case 'H':
      return "Hacking away at challenges until they're conquered."
    case 'M':
      return 'Mastering the art of code and the science of systems.'
    case 'I':
      return 'Innovating to drive progress and push boundaries.'
    case 'D':
      return 'Dedicated to delivering quality software and solutions.'
    default:
      return 'I code with passion and purpose, striving for excellence in everything I do.'
  }
}
